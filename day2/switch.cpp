#include <bits/stdc++.h>
using namespace std;
int main()
{
    int day = 4;
    switch (day)
    {
    case 4:
        cout << "Today is Thursday";
        break;
    case 5:
        cout << "Today is Friday";
        break;
    case 6:
        cout << "Today is Saturday";
        break;
    case 7:
        cout << "Today is Sunday";
        break;
    default:
        cout << "Looking forward to the Weekend";
    }
}